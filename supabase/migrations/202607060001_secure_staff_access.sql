begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'profiles_role_check'
      and conrelid = 'public.profiles'::regclass
  ) then
    alter table public.profiles
      add constraint profiles_role_check
      check (role in ('pending', 'admin', 'champion', 'organization')) not valid;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'reports_status_check'
      and conrelid = 'public.reports'::regclass
  ) then
    alter table public.reports
      add constraint reports_status_check
      check (status in ('Reported', 'Verified', 'Assigned', 'In Progress', 'Resolved', 'Community Confirmed')) not valid;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'reports_risk_score_check'
      and conrelid = 'public.reports'::regclass
  ) then
    alter table public.reports
      add constraint reports_risk_score_check
      check (risk_score is null or (risk_score >= 0 and risk_score <= 100)) not valid;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'access_requests_status_check'
      and conrelid = 'public.access_requests'::regclass
  ) then
    alter table public.access_requests
      add constraint access_requests_status_check
      check (status in ('Pending', 'Approved', 'Rejected')) not valid;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'access_requests_requested_role_check'
      and conrelid = 'public.access_requests'::regclass
  ) then
    alter table public.access_requests
      add constraint access_requests_requested_role_check
      check (requested_role in ('champion', 'organization')) not valid;
  end if;
end $$;


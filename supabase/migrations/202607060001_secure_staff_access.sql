create or replace function public.reject_access_request(request_id text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_request public.access_requests%rowtype;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  select *
  into v_request
  from public.access_requests
  where id::text = request_id
  limit 1;

  if not found then
    raise exception 'Access request not found' using errcode = 'P0002';
  end if;

  if v_request.status <> 'Pending' then
    raise exception 'Only pending access requests can be rejected' using errcode = 'P0001';
  end if;

  update public.access_requests
  set
    status = 'Rejected',
    reviewed_by = auth.uid(),
    reviewed_at = now()
  where id::text = request_id
    and status = 'Pending';

  if not found then
    raise exception 'Access request was already reviewed' using errcode = 'P0001';
  end if;

  return jsonb_build_object(
    'success', true,
    'request_id', request_id,
    'status', 'Rejected'
  );
end;
$$;

revoke all on function public.reject_access_request(text) from public;
grant execute on function public.reject_access_request(text) to authenticated;

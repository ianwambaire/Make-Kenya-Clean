-- Make Kenya Clean staff access hardening.
--
-- This migration is intentionally conservative for an existing live Supabase
-- project. It does not drop tables, truncate data, rewrite primary keys, or
-- force column type conversions. If an existing column has a different type,
-- PostgreSQL will keep it as-is and these ADD COLUMN IF NOT EXISTS statements
-- will not alter it.


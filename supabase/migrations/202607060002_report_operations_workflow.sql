create or replace function public.add_report_action(
  p_assignment_id bigint,
  p_action_type text,
  p_note text,
  p_visibility text default 'Public'
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_assignment public.report_assignments%rowtype;
  v_report public.reports%rowtype;
  v_action public.report_actions%rowtype;
  v_actor_name text;
  v_visibility text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  select * into v_assignment
  from public.report_assignments
  where id = p_assignment_id;

  if not found then
    raise exception 'Assignment not found' using errcode = 'P0002';
  end if;

  if not public.is_organization_member(v_assignment.organization_id) then
    raise exception 'Assigned organization role required' using errcode = '42501';
  end if;

  if v_assignment.status <> 'Accepted' then
    raise exception 'Actions can only be added to accepted assignments' using errcode = 'P0001';
  end if;

  select * into v_report
  from public.reports
  where id = v_assignment.report_id;

  if v_report.status not in ('In Progress', 'Resolution Submitted') then
    raise exception 'Report is not active for operational updates' using errcode = 'P0001';
  end if;

  v_visibility := case when p_visibility = 'Staff Only' then 'Staff Only' else 'Public' end;

  insert into public.report_actions (
    report_id,
    assignment_id,
    organization_id,
    action_type,
    note,
    visibility,
    created_by
  )
  values (
    v_assignment.report_id,
    p_assignment_id,
    v_assignment.organization_id,
    coalesce(nullif(p_action_type, ''), 'Other'),
    coalesce(p_note, ''),
    v_visibility,
    auth.uid()
  )
  returning * into v_action;

  if v_visibility = 'Public' then
    v_actor_name := public.assignment_actor_name(auth.uid());

    insert into public.report_updates (
      report_id,
      status,
      note,
      updated_by,
      updated_by_name
    )
    values (
      v_assignment.report_id,
      v_action.action_type,
      coalesce(nullif(v_action.note, ''), 'Operational update added.'),
      auth.uid(),
      v_actor_name
    );
  end if;

  return jsonb_build_object(
    'success', true,
    'action_id', v_action.id,
    'assignment_id', p_assignment_id
  );
end;
$$;


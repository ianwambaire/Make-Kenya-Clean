create or replace function public.assign_report_to_organization(
  p_report_id bigint,
  p_organization_id uuid,
  p_note text default ''
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_report public.reports%rowtype;
  v_organization public.organizations%rowtype;
  v_assignment public.report_assignments%rowtype;
  v_actor_name text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  select * into v_report
  from public.reports
  where id = p_report_id
  for update;

  if not found then
    raise exception 'Report not found' using errcode = 'P0002';
  end if;

  if v_report.status <> 'Verified' then
    raise exception 'Only verified reports can be assigned' using errcode = 'P0001';
  end if;

  select * into v_organization
  from public.organizations
  where id = p_organization_id
    and status = 'Active';

  if not found then
    raise exception 'Active organization not found' using errcode = 'P0002';
  end if;

  if exists (
    select 1
    from public.report_assignments
    where report_id = p_report_id
      and status in ('Assigned', 'Accepted')
  ) then
    raise exception 'Report already has an active assignment' using errcode = 'P0001';
  end if;

  insert into public.report_assignments (
    report_id,
    organization_id,
    assigned_by,
    note
  )
  values (
    p_report_id,
    p_organization_id,
    auth.uid(),
    coalesce(p_note, '')
  )
  returning * into v_assignment;

  update public.reports
  set status = 'Assigned'
  where id = p_report_id;

  v_actor_name := public.assignment_actor_name(auth.uid());

  insert into public.report_updates (
    report_id,
    status,
    note,
    updated_by,
    updated_by_name
  )
  values (
    p_report_id,
    'Assigned',
    'Assigned to ' || v_organization.name || case when coalesce(p_note, '') <> '' then ': ' || p_note else '.' end,
    auth.uid(),
    v_actor_name
  );

  return jsonb_build_object(
    'success', true,
    'assignment_id', v_assignment.id,
    'report_id', p_report_id,
    'organization_id', p_organization_id,
    'status', 'Assigned'
  );
end;
$$;


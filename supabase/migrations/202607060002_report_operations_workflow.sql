create or replace function public.can_write_resolution_evidence_object(object_name text)
returns boolean
language plpgsql
stable
security definer
set search_path = public, storage
as $$
declare
  v_parts text[];
  v_organization_id uuid;
  v_assignment_id bigint;
begin
  if auth.uid() is null then
    return false;
  end if;

  if public.current_profile_role() <> 'organization' then
    return false;
  end if;

  v_parts := storage.foldername(object_name);

  if array_length(v_parts, 1) < 2 then
    return false;
  end if;

  begin
    v_organization_id := v_parts[1]::uuid;
    v_assignment_id := v_parts[2]::bigint;
  exception when others then
    return false;
  end;

  if not public.is_organization_member(v_organization_id) then
    return false;
  end if;

  return exists (
    select 1
    from public.report_assignments assignment
    join public.reports report on report.id = assignment.report_id
    where assignment.id = v_assignment_id
      and assignment.organization_id = v_organization_id
      and assignment.status = 'Accepted'
      and report.status = 'In Progress'
  );
end;
$$;


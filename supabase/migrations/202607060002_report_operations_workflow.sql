create or replace function public.review_resolution_evidence(
  p_evidence_id bigint,
  p_approved boolean,
  p_review_note text default ''
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_evidence public.resolution_evidence%rowtype;
  v_actor_name text;
  v_new_report_status text;
  v_assignment_status text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  select * into v_evidence
  from public.resolution_evidence
  where id = p_evidence_id
  for update;

  if not found then
    raise exception 'Resolution evidence not found' using errcode = 'P0002';
  end if;

  if v_evidence.review_status <> 'Submitted' then
    raise exception 'Resolution evidence has already been reviewed' using errcode = 'P0001';
  end if;

  v_new_report_status := case when p_approved then 'Resolved' else 'In Progress' end;
  v_assignment_status := case when p_approved then 'Completed' else 'Accepted' end;

  update public.resolution_evidence
  set
    review_status = case when p_approved then 'Approved' else 'Rejected' end,
    reviewed_by = auth.uid(),
    reviewed_at = now(),
    review_note = coalesce(p_review_note, '')
  where id = p_evidence_id;

  update public.report_assignments
  set
    status = v_assignment_status,
    updated_at = now()
  where id = v_evidence.assignment_id;

  update public.reports
  set status = v_new_report_status
  where id = v_evidence.report_id;

  v_actor_name := public.assignment_actor_name(auth.uid());

  insert into public.report_updates (
    report_id,
    status,
    note,
    updated_by,
    updated_by_name
  )
  values (
    v_evidence.report_id,
    v_new_report_status,
    case
      when p_approved then coalesce(nullif(p_review_note, ''), 'Resolution evidence approved.')
      else coalesce(nullif(p_review_note, ''), 'Resolution evidence needs more work.')
    end,
    auth.uid(),
    v_actor_name
  );

  return jsonb_build_object(
    'success', true,
    'evidence_id', p_evidence_id,
    'report_id', v_evidence.report_id,
    'status', v_new_report_status
  );
end;
$$;

create or replace function public.submit_community_confirmation(
  p_tracking_code text,
  p_confirmation text,
  p_note text default ''
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_report public.reports%rowtype;
  v_confirmation text;
  v_tracking_code text;
  v_note text;
  v_confirmation_row public.community_confirmations%rowtype;
begin
  v_tracking_code := upper(trim(coalesce(p_tracking_code, '')));
  v_note := left(trim(coalesce(p_note, '')), 500);

  if v_tracking_code = '' or length(v_tracking_code) > 64 then
    raise exception 'A valid tracking code is required' using errcode = 'P0001';
  end if;

  if p_confirmation not in ('Confirmed', 'Disputed') then
    raise exception 'Confirmation must be Confirmed or Disputed' using errcode = 'P0001';
  end if;

  v_confirmation := p_confirmation;

  select * into v_report
  from public.reports
  where tracking_code = v_tracking_code
  for update;

  if not found then
    raise exception 'Report not found' using errcode = 'P0002';
  end if;

  if v_report.status <> 'Resolved' then
    raise exception 'Only resolved reports can receive community confirmation' using errcode = 'P0001';
  end if;

  if exists (
    select 1
    from public.community_confirmations
    where report_id = v_report.id
      and review_status = 'Pending'
  ) then
    raise exception 'Community feedback is already pending review for this report' using errcode = 'P0001';
  end if;

  insert into public.community_confirmations (
    report_id,
    tracking_code,
    confirmation,
    note,
    review_status
  )
  values (
    v_report.id,
    v_report.tracking_code,
    v_confirmation,
    v_note,
    'Pending'
  )
  returning * into v_confirmation_row;

  insert into public.report_updates (
    report_id,
    status,
    note,
    updated_by_name
  )
  values (
    v_report.id,
    'Community Feedback Submitted',
    case
      when v_confirmation = 'Confirmed' then coalesce(nullif(v_note, ''), 'Community feedback submitted for admin review.')
      else coalesce(nullif(v_note, ''), 'Community dispute submitted for admin review.')
    end,
    'Community Feedback'
  );

  return jsonb_build_object(
    'success', true,
    'confirmation_id', v_confirmation_row.id,
    'report_id', v_report.id,
    'confirmation', v_confirmation,
    'review_status', 'Pending',
    'status', v_report.status
  );
end;
$$;

create or replace function public.review_community_confirmation(
  p_confirmation_id bigint,
  p_decision text,
  p_review_note text default ''
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_confirmation public.community_confirmations%rowtype;
  v_actor_name text;
  v_new_report_status text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  if p_decision not in ('Approved', 'Rejected') then
    raise exception 'Decision must be Approved or Rejected' using errcode = 'P0001';
  end if;

  select * into v_confirmation
  from public.community_confirmations
  where id = p_confirmation_id
  for update;

  if not found then
    raise exception 'Community confirmation not found' using errcode = 'P0002';
  end if;

  if v_confirmation.review_status <> 'Pending' then
    raise exception 'Community confirmation has already been reviewed' using errcode = 'P0001';
  end if;

  update public.community_confirmations
  set
    review_status = p_decision,
    reviewed_by = auth.uid(),
    reviewed_at = now(),
    review_note = left(coalesce(p_review_note, ''), 1000)
  where id = p_confirmation_id;

  v_actor_name := public.assignment_actor_name(auth.uid());

  if p_decision = 'Approved' then
    v_new_report_status := case
      when v_confirmation.confirmation = 'Confirmed' then 'Community Confirmed'
      else 'In Progress'
    end;

    update public.reports
    set status = v_new_report_status
    where id = v_confirmation.report_id
      and status = 'Resolved';

    if not found then
      raise exception 'Report must still be Resolved to approve community feedback' using errcode = 'P0001';
    end if;

    if v_confirmation.confirmation = 'Disputed' then
      update public.report_assignments
      set
        status = 'Accepted',
        updated_at = now()
      where report_id = v_confirmation.report_id
        and status = 'Completed';
    end if;

    insert into public.report_updates (
      report_id,
      status,
      note,
      updated_by,
      updated_by_name
    )
    values (
      v_confirmation.report_id,
      v_new_report_status,
      case
        when v_confirmation.confirmation = 'Confirmed' then coalesce(nullif(p_review_note, ''), 'Admin approved community confirmation.')
        else coalesce(nullif(p_review_note, ''), 'Admin approved community dispute and returned the case to active work.')
      end,
      auth.uid(),
      v_actor_name
    );
  else
    insert into public.report_updates (
      report_id,
      status,
      note,
      updated_by,
      updated_by_name
    )
    values (
      v_confirmation.report_id,
      'Community Feedback Rejected',
      coalesce(nullif(p_review_note, ''), 'Admin rejected community feedback.'),
      auth.uid(),
      v_actor_name
    );
  end if;

  return jsonb_build_object(
    'success', true,
    'confirmation_id', p_confirmation_id,
    'report_id', v_confirmation.report_id,
    'decision', p_decision,
    'status', coalesce(v_new_report_status, 'Resolved')
  );
end;
$$;


create or replace function public.create_organization(
  p_name text,
  p_organization_type text default 'Other',
  p_area text default '',
  p_contact_email text default '',
  p_contact_phone text default ''
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_organization public.organizations%rowtype;
  v_type text;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  if length(trim(coalesce(p_name, ''))) < 2 then
    raise exception 'Organization name is required' using errcode = 'P0001';
  end if;

  v_type := case
    when p_organization_type in ('County', 'NGO', 'School', 'Estate', 'Institution', 'Utility', 'Community Organization', 'Other') then p_organization_type
    else 'Other'
  end;

  insert into public.organizations (
    name,
    organization_type,
    area,
    contact_email,
    contact_phone,
    status,
    created_by
  )
  values (
    trim(p_name),
    v_type,
    left(trim(coalesce(p_area, '')), 160),
    left(lower(trim(coalesce(p_contact_email, ''))), 254),
    left(trim(coalesce(p_contact_phone, '')), 80),
    'Active',
    auth.uid()
  )
  returning * into v_organization;

  return jsonb_build_object('success', true, 'organization_id', v_organization.id);
end;
$$;


create or replace function public.assign_profile_to_organization(
  p_profile_id uuid,
  p_organization_id uuid
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_profile public.profiles%rowtype;
  v_organization public.organizations%rowtype;
begin
  if auth.uid() is null then
    raise exception 'Authentication required' using errcode = '28000';
  end if;

  if not public.is_admin() then
    raise exception 'Admin role required' using errcode = '42501';
  end if;

  select * into v_profile
  from public.profiles
  where id = p_profile_id
  for update;

  if not found then
    raise exception 'Profile not found' using errcode = 'P0002';
  end if;

  if v_profile.role <> 'organization' then
    raise exception 'Only organization profiles can be assigned to organizations' using errcode = 'P0001';
  end if;

  select * into v_organization
  from public.organizations
  where id = p_organization_id;

  if not found then
    raise exception 'Organization not found' using errcode = 'P0002';
  end if;

  update public.profiles
  set organization_id = p_organization_id,
      organization_name = v_organization.name,
      updated_at = now()
  where id = p_profile_id;

  return jsonb_build_object('success', true, 'profile_id', p_profile_id, 'organization_id', p_organization_id);
end;
$$;

